    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        person = new Provider();
         if (validation.checkOpenFile(new File("Provider.csv"))==false) {
            validationOfFullData();
            RUCVerificationForSaving = validation.IDVerification(txtRUC);
            if(RUCVerificationForSaving==true){
                person.setRuc(txtRUC.getText());
                if(txtName.getBackground() == Color.red||txtCompany.getBackground()==Color.red){
                    showMessageDialog(null, "Revise los parametros erroneos");
                }else{
                    person.setNameProvider(txtName.getText());
                    person.setNameCompany(txtCompany.getText());
                    if(txtHomePhone.getBackground()== Color.red||txtMobile.getBackground()==Color.red){
                        showMessageDialog(null, "Revise los parametros erroneos");
                    }else{
                        person.setPhoneNumber(txtHomePhone.getText());
                        person.setPhoneNumber2(txtMobile.getText());
                        auxStringForFile = toString();
                        //showMessageDialog(null, auxStringForFile);
                        String imput=txtName.getText()+";"+txtCompany.getText()+";"+
                                txtRUC.getText()+";"+txtMobile.getText()+";"+txtHomePhone.getText()+"\n";
                        FileManager.writeFile(new File("Provider.csv"), imput);
                        JOptionPane.showMessageDialog(this, "se ha agregado satisfactoriamente al archivo", 
                                "Agregado", JOptionPane.INFORMATION_MESSAGE);
                        cleanCells();
                    }
                }
            }else {
                showMessageDialog(null, "Revise los parametros erroneos");
            }     
         }else {
            showMessageDialog(null, "El archivo de datos se encuentra siendo usado por otra aplicaciÃ³n");
        }
    }//GEN-LAST:event_btnSearchActionPerformed
    public void validationOfFullData(){
        if (txtCompany.getText().equals("")||txtHomePhone.getText().equals("")
                ||txtMobile.getText().equals("")||txtName.getText().equals("")||txtRUC.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Compos Vacios Verifique porfavor");   
            return;
        }
    }
